public class RecordMonth {
    String itemName;
    String isExpense;
    int quantity;
    double sumOfOne;

    RecordMonth(String newName, String newIsExpense, int newQuantity, double newSumOfOne) {
        itemName = newName;
        isExpense = newIsExpense;
        quantity = newQuantity;
        sumOfOne = newSumOfOne;
    }
}
